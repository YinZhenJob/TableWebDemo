<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">在这本书里，马未都、史航、赵赵、徐德亮、田原、钟伟民、张佳炜、苏枕书、寂地、罗尘等讲述“猫与我”的生活。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“不是我养它，更像它陪我。”</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“爱它，就是追寻一种永不满足的亲密。”</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“一时冲动，生命就布满白毛。”</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“它们一边鄙视，一边用自己的方式照顾我们。”</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“我把猫儿当朵花，猫儿把我当傻瓜。”</span><span style="font-family: 宋体, SimSun; text-indent: 2em;">&nbsp;</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">还有日本“网红”猫 Pokke 主人饭塚水叶独家授权她记录这只猫的9年萌态。在养 Pokke 之前，饭塚水叶觉得猫不太温柔，需要主人单方面的情感投入。而养了 Pokke后，她说：“它虽然不能够用语言表达，但我可以感受到它非常多的情绪……我不是一个很文艺的人，但是通过猫，我想我明白了感情到底是怎么一回事。”&nbsp;</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183732396882.jpg" title="1471183732396882.jpg" alt="image-6.jpg"></p>
<p style="text-align: center; text-indent: 2em;"><span style="font-family: 微软雅黑, 'Microsoft YaHei'; text-indent: 2em; font-size: 12px;">日本最红的网红猫 Pokke 晒了一张和《目客004：猫：懒得理你》的合影。</span>
<br><span style="font-size: 16px; font-family: 宋体, SimSun;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">此外，从污名到疗愈，Lens 梳理了东西方社会中猫之名声演变，并提到了日本浮世绘中的猫与日本人的生活，以及一些中国文人和猫的故事：常玉、夏衍、丰子恺、季羡林、徐志摩……</span><span style="font-family: 宋体, SimSun; text-indent: 2em;">&nbsp;</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">最后有37个“为什么”带你进入猫的秘密世界:</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">猫为什么喜欢蹭来蹭去？</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">每天待在家里的猫会觉得无聊吗？</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">为什么猫每天要吃那么多餐饭？</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">猫为什么爱把桌子上的东西都扫到地上？</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">……</span><span style="font-family: 宋体, SimSun; text-indent: 2em;">&nbsp;</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">看完猫书，可以翻翻附赠的别册《一只猫的自白》，看看一只办公室猫带来的减压对话……</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183767127985.jpg" title="1471183767127985.jpg" alt="image-7.jpg"></p>
<p style="text-align: center; text-indent: 2em;"><span style="font-family: 微软雅黑, 'Microsoft YaHei'; text-indent: 2em; font-size: 12px;">每本《猫：懒得理你》中都附赠一本《一只猫的自白》</span>
<br><span style="font-size: 16px; font-family: 宋体, SimSun;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">此外，以下关于喵星人的书，也推荐给各位铲屎官。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">《小猫杜威》</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183789917265.jpg" title="1471183789917265.jpg" alt="image-8.jpg"></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">这是一个感人的故事，看完这本书，会产生一种冲动，想亲自去见见这只小猫，抱抱它，抚摸它！</span>
<br><span style="font-family: 宋体, SimSun; text-indent: 2em;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">那年最冷的一个晚上，小猫杜威出生只有几周，就被丢进斯潘塞图书馆的还书箱。图书馆馆长薇奇·麦仑是单身妈妈，以前的丈夫是个酒鬼，遭受着自家农场的破产和罹患乳腺癌的痛苦，但仍然坚强地生活着。第二天早晨，她发现了小猫杜威。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">小猫杜威获救后勉强撑起一瘸一拐的腿脚，以充满感激和爱的姿势，磨蹭每一只抚摸它的手，赢得了薇奇的心，也赢得了图书馆所有人的心。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">在随后的十九年里，小猫杜威用热情、温暖、谦虚和敏锐的第六感（小猫杜威总是知道谁最需要它），感动和吸引着小镇居民。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">小猫杜威的名气，从一个小镇传到另一个小镇，从一个国家到另一个国家，最终闻名世界。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">小猫杜威不再只是一个朋友，它成为了人们的骄傲，成了逐渐从有史以来最大的危机中恢复过来的小镇的骄傲。</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183806104838.jpg" title="1471183806104838.jpg" alt="image-9.jpg"></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">如果你看过怀特所写的《夏洛的网》，就会发现那里面的蜘蛛夏洛和小猫杜威有相同之处：热情，诚实，乐观，坚韧，还有更重要的，它是每一个人的朋友。美国有几十只图书馆猫，但它们谁也没有做出杜威这样的成绩。它不仅是漂亮，身世特殊，它也不是一只普通的、让人们抚摸和微笑的猫。每个定期光临图书馆的人，每一个人，都觉得他们跟杜威有着不一般的关系。杜威让每个人都觉得特殊。这对于生活在困苦艰难时期的人来说，是一份极大的心理安慰。</span>
<br><span style="font-size: 16px; font-family: 宋体, SimSun;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">找到你的位置，满足于你拥有的东西，善待每一个人，过一种好的生活。不是关于物质，而是关于爱。爱，永远是可遇不可求的。这就是小猫杜威要告诉我们的。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">《猫咪绘:33只萌猫的色铅笔图绘》</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183823559671.jpg" title="1471183823559671.jpg" alt="image-10.jpg"></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">猫咪是我们最亲密的伙伴之一，在我们孤单寂寞的时候陪伴我们左右。它们晶莹得让人陶醉的眼睛像宝石一样美丽，还有柔软的爪子和蓬松的毛发，几乎任何一个地方都能博得人们的喜爱，猫咪的这些萌点让它们的一举一动都打动着我们。</span>
<br><span style="font-family: 宋体, SimSun; text-indent: 2em;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">通过《猫咪绘:33只萌猫的色铅笔图绘》，我们可以跟着案例一笔一笔地画出生动、有趣的猫咪，而且还能从中认识到很多的猫咪品种。</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183840611997.jpg" title="1471183840611997.jpg" alt="image-11.jpg"></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">准备好一盒彩色铅笔，一张画纸，留出一小段休闲的时间，再泡上一杯香浓的咖啡，一起来走进猫咪的世界，用满满的爱来描绘出它们美丽可爱的模样吧！</span>
<br><span style="font-size: 16px; font-family: 宋体, SimSun;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">《猎人们》</span></p>
<p style="text-align:center"><img src="http://www.duzhe.com/uploadfile/2016/08/14/1471183862128299.jpg" title="1471183862128299.jpg" alt="image-12.jpg"></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-family: 宋体, SimSun; text-indent: 2em;">《猎人们》是朱天心的经典文集，描述了她及家人与猫族共处的生活。作者描写了各种性格迥异的猫，包括并不那么可爱的猫。她养猫的方式是放养，尊重猫的天性，不想因为主人的性格去改变他们，于是猫猫来去自由，随便所欲；同时她也喂养很多流浪猫，极大程度极大视野的看到了猫的世界。这不仅是温暖的人族与喵族的交往，更提出要将所有生灵视为平等的观念，用领养代替购买；用定点定时喂食、结扎、放生来拯救流浪喵儿们。</span>
<br><span style="font-family: 宋体, SimSun; text-indent: 2em;"></span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">作者看似平淡的叙述，却时时击中人心底最柔软的部分。</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">“我心存感激，感激这些如此狂野独行的猎人们，愿意不时与我暂处同一个屋檐下；在这每天都有天灾人祸、人命百条千条死去的现下，我简直无法对别人倾诉一只街猫的离去和与我的短暂际遇。”</span></p>
<p style="text-align: left; text-indent: 2em;"><span style="font-size: 16px; font-family: 宋体, SimSun;">小编看到李家宝一篇时，潸然泪下，仿佛李家宝不是一只猫，就是一个活生生的人，有自己的喜怒哀乐。</span><span style="font-family: 宋体, SimSun; text-indent: 2em;">&nbsp;</span></p>
